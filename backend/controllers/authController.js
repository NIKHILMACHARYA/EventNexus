// @desc    Create admin account (requires secret key)
// @route   POST /api/auth/create-admin
// @access  Public (but requires ADMIN_SECRET_KEY)
exports.createAdmin = async (req, res) => {
  try {
    const { name, email, password, college, secretKey } = req.body;

    // Verify secret key from environment
    if (!process.env.ADMIN_SECRET_KEY || secretKey !== process.env.ADMIN_SECRET_KEY) {
      return res.status(403).json({
        success: false,
        message: 'Invalid admin secret key'
      });
    }

    // Check if user exists
    const existingUser = await User.findByEmail(email);
    if (existingUser) {
      return res.status(400).json({
        success: false,
        message: 'User already exists with this email'
      });
    }

    // Create admin user
    const user = await User.create({
      name,
      email,
      password,
      college,
      role: 'admin'
    });

    sendTokenResponse(user, 201, res);
  } catch (error) {
    console.error(error);
    res.status(500).json({
      success: false,
      message: 'Server error'
    });
  }
};

// @desc    Promote existing user to admin (admin only)
// @route   PUT /api/auth/promote/:userId
// @access  Private/Admin
exports.promoteToAdmin = async (req, res) => {
  try {
    const user = await User.findById(req.params.userId);
    
    if (!user) {
      return res.status(404).json({
        success: false,
        message: 'User not found'
      });
    }

    if (user.role === 'admin') {
      return res.status(400).json({
        success: false,
        message: 'User is already an admin'
      });
    }

    const updatedUser = await User.update(req.params.userId, { role: 'admin' });

    res.status(200).json({
      success: true,
      message: `${user.name} has been promoted to admin`,
      data: updatedUser
    });
  } catch (error) {
    console.error(error);
    res.status(500).json({
      success: false,
      message: 'Server error'
    });
  }
};

// Helper function to get token and send response
const sendTokenResponse = (user, statusCode, res) => {
  const token = User.getSignedJwtToken(user.id);

  res.status(statusCode).json({
    success: true,
    token,
    user: {
      id: user.id,
      name: user.name,
      email: user.email,
      role: user.role,
      college: user.college,
      avatar: user.avatar
    }
  });
};
